package org.example.two;

public class Basket {
    private int apples;
    private int mangoes;

    public Basket(int apples, int mangoes) {
        this.apples = apples;
        this.mangoes = mangoes;
    }

    public int getApples() {
        return apples;
    }

    public int getMangoes() {
        return mangoes;
    }
}
